### Ejercicio: Booleanos

#Verifica si la raíz cuadrada de 25 es igual a 65/13 y muestra el resultado (booleano) en pantalla utilizando `print()`

raiz_cuadrada = 25 ** (1/2) # Calcular la raíz cuadrada de 25
division = 65 / 13 # Calcular 65 dividido entre 13
resultado = raiz_cuadrada == division # Comparar si la raíz cuadrada es igual a la división
print(resultado) # Imprimir el resultado
