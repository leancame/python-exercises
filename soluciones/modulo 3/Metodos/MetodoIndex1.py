### Ejercicio: Obtener ubicación de caracter
#Encuentra y muestra en pantalla qué caracter ocupa la quinta posición dentro de la siguiente palabra: "orde*N*ador"



# Solución

palabra = "orde*N*ador"
letra = palabra[4] # La posición 4 corresponde a la quinta letra, ya que el índice comienza en 0
print(f"La letra que ocupa la quinta posición es: {letra}") # Imprime la letra en la posición 4
