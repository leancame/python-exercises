### Ejercicio: Extracción
#Extrae los elementos de la siguiente tupla en cuatro variables:


'''
> a, b, c, d
	
```python
	mi_tupla = (1, 2, 3, 4)
```
'''
# Solución:
mi_tupla = (1, 2, 3, 4)
a = mi_tupla[0] # Acceso al primer elemento
b = mi_tupla[1] # Acceso al segundo elemento
c = mi_tupla[2] # Acceso al tercer elemento
d = mi_tupla[3] # Acceso al cuarto elemento
print(a, b, c, d) # Imprime los valores de las variables

